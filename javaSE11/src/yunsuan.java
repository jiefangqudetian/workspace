public class yunsuan {
	
	public int jia(int numA,int numB) {
		
		int result = numA + numB;
		return result;
	}
	public int jian(int numA,int numB) {
		
		int result = numA - numB;
		return result;
	}
	public int cheng(int numA,int numB) {
		
		int result = numA * numB;
		return result;
	}
	public int chu(int numA,int numB) {
		int result = numA / numB;
		return result;
	}
	
}